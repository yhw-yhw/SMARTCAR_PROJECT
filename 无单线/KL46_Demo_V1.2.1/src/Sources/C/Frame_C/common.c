//-------------------------------------------------------------------------*
//函数名: stop                                                             *
//功  能: 设置CPU为STOP模式                                                * 
//参  数: 无								   *	
//返  回: 无                                                               *
//说  明: 无                                                               *
//-------------------------------------------------------------------------*
void stop (void)
{
    //置位SLEEPDEEP来使能STOP模式
    SCB_SCR |= SCB_SCR_SLEEPDEEP_MASK;	
    //进入STOP模式
    asm("WFI");
}

//-------------------------------------------------------------------------*
//函数名: wait                                                             *
//功  能: 设置CPU为WAIT模式                                                * 
//参  数: 无								   *	
//返  回: 无                                                               *
//说  明: 无                                                               *
//-------------------------------------------------------------------------*
void wait (void)
{
    //清SLEEPDEEP位来确定进入WAIT模式
    SCB_SCR &= ~SCB_SCR_SLEEPDEEP_MASK;	
    //进入WAIT模式
    asm("WFI");
}

