//函数名: enable_irq                                                       *
//功  能: 使能irq中断                                                      * 
//参  数: irq:irq号       						   *	
//返  回: 无                                                               *
//说  明: irq号不是中断向量号                                              *
//-------------------------------------------------------------------------*
void enable_irq (int irq)
{
    if (irq > 32)
    {
            ;// printf("\nERR! Invalid IRQ value passed to enable irq function!\n");
      
    }
    else
    {
      /* Set the ICPR and ISER registers accordingly */
      NVIC_ICPR |= 1 << (irq%32);
      NVIC_ISER |= 1 << (irq%32);
    }
}

